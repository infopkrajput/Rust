fn main() {
    // printing styles in Rust
    print!("Hello, world!"); // Prints the string to console without a newline
    println!("Hello, world!"); // prints the string to console with a newline
    eprint!("Hello, world!"); // Prints anything within the parentheses as an error message
    eprintln!("Hello, world!"); // prints anything within the parentheses as an error message with a newline
}
